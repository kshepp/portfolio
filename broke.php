<h2>What is the Broke App?</h2>

<h4>
The Broke App is a mobile, personal finance application for banks to reach and teach college students how 
to build wealth for the future based on their current spending habits.
<br />
<br />
For more information you can check out our <a href="http://www.thebrokeapp.com">website.</a>
