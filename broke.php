<h3>Broke @ TechTown Detroit</h3>

<p>
For my internship in the summer of 2015, I participated in TechTown Detroit's DTX Launch program, 
  which is a 10 week student accelerator program on customer discovery. During my time in TechTown and TechArb, 
  where The Broke App incubates, I completed the following:
</p>
<ul>
<li>Awarded the 'Get Set' award from TechTown and Bank of America for diligence in the customer discovery process</li>
<li>Created a viable business canvas model</li>
<li>Completed over 100 interviews with students, banks, financial literacy experts, investment firms, and 
other entrepreneurs in the FinTech space</li>
<li>Pitched weekly to members of the New Enterprise Forum of Michigan to receive feedback and refine 
business canvas model</li>
<li>Created and tested lo-fi and hi-fi prototypes (Sketch) to verify value provided as given in customer feedback</li>
</ul>
