<h2>Design Challenges</h2>
<p>
  "Ethical fashion" and "clothing made with dignity" as phrases are not a widely 
  used by the American consumer base. By using these terms I'm refering to whether
  the producers of the clothing receive a living wage.
   As a designer and marketer, I work to connect
  producers and consumers through an experience on the BetterHope site. BetterHope is
   challenged to show that ethical clothing can be fashionable and affordable.
</p>

<h2>Projects</h2>

<h3><em>Product Collages</em></h3>
<p>
  Part of my work at BetterHope includes creating product collages to highlight combination of
  products on the site to create stylish outfits that fit any occasion. Often "ethical fashion"
  can be seen as a "pity purchase" or have a "trinkety" feel, which is a 
  steryeotype we try to fight through the collages.
  <br />
  <br />
  Examples of some of the collages can be seen above as well as on the 
  <a href="http://www.betterhope.is">BetterHope </a> site
</p>

